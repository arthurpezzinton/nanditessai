function get_translation($text = null){
    

    $language = get_language_cookie();

    if(!array_key_exists($language,$GLOBALS['languages'])) $language = $GLOBALS['default_language'];
    if(!array_key_exists($text,$GLOBALS['languages'][$language])) $text = "not_identified_translation";

    $text = $GLOBALS['languages'][$language][$text];

    return $text;
}
